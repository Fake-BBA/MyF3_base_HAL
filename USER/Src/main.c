int main(void)
{
	HAL_Init();				//初始化库
	SystemClock_Config();	//重新设置时钟为72M
	SysTick_Init();	//
	Sys_LED_Init();
	Buzzer_Init();
	MX_DMA_Init();
	MX_I2C1_Init();
	USART1_UART_Init(1000000);
	Init_MPU6050();
	//USART3_UART_Init();
	
	struct TimerTemp t1;
	ReSetTimerTemp(&t1);
	char sendBuff[100];
	uint32 t2;
	
	struct AccData acc;
	struct GyroData gyro;
	struct FlightState state;
	while (1)
	{
		
		if(WaitSysTime_UnBlocked(&t1,2,UINT_MS))
		{
			ReSetTimerTemp(&t1);
			Sys_LED_Negative();
			Read_MPU6050();
			
			acc.x=ACC_X;
			acc.y=ACC_Y;
			acc.z=ACC_Z;
			
			gyro.x=GYR_X;
			gyro.y=GYR_Y;
			gyro.z=GYR_Z;
			
			imuUpdate(acc,gyro,&state,2);
			//snprintf(sendBuff,100,"ACC_X:%d\tACC_Y:%d\tACC_Z:%d\r\n Temp:%f\r\n",ACC_X,ACC_Y,ACC_Z,MPU6050_TEMP);
			UART1_SendBytes(sendBuff,strlen(sendBuff));
			DMA_UART1_SendThread();		
		}
		
	}
	
}

void SystemClock_Config(void)
{

  RCC_OscInitTypeDef RCC_OscInitStruct;
  RCC_ClkInitTypeDef RCC_ClkInitStruct;
  RCC_PeriphCLKInitTypeDef PeriphClkInit;

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSI|RCC_OSCILLATORTYPE_HSE;
  RCC_OscInitStruct.HSEState = RCC_HSE_ON;
  RCC_OscInitStruct.HSEPredivValue = RCC_HSE_PREDIV_DIV1;
  RCC_OscInitStruct.HSIState = RCC_HSI_ON;
  RCC_OscInitStruct.HSICalibrationValue = 16;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_ON;
  RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSE;
  RCC_OscInitStruct.PLL.PLLMUL = RCC_PLL_MUL9;
  HAL_RCC_OscConfig(&RCC_OscInitStruct);

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK
                              |RCC_CLOCKTYPE_PCLK1|RCC_CLOCKTYPE_PCLK2;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_PLLCLK;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV2;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV1;

  HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_2);

  PeriphClkInit.PeriphClockSelection = RCC_PERIPHCLK_USART3|RCC_PERIPHCLK_I2C1;
  PeriphClkInit.Usart3ClockSelection = RCC_USART3CLKSOURCE_PCLK1;
  PeriphClkInit.I2c1ClockSelection = RCC_I2C1CLKSOURCE_HSI;
  HAL_RCCEx_PeriphCLKConfig(&PeriphClkInit);

    /**Configure the Systick interrupt time 
    */
  HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq()/1000);

    /**Configure the Systick 
    */
  HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK);

  /* SysTick_IRQn interrupt configuration */
  HAL_NVIC_SetPriority(SysTick_IRQn, 0, 0);
}
